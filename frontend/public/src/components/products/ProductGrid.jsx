import ProductCard from "./ProductCard";

function ProductGrid({ products }) {
  return (
    <div className="grid md:grid-cols-3 lg:grid-cols-4 gap-6">
      {products.map((product) => (
        <ProductCard
          key={product.id}
          product={product}
        />
      ))}
    </div>
  );
}

export default ProductGrid;